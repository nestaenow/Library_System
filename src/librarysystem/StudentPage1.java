    private void stdloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stdloginActionPerformed
        // TODO add your handling code here:
        String pass = stdPass.getText();
        try{
            ps = con.prepareStatement("select First_Name, Matricule from login where First_Name = '"+stdName.getText()+"'");
            rs = ps.executeQuery();
            if(rs.next()){
                if(pass.equals(rs.getString("Matricule"))){
                JOptionPane.showMessageDialog(null, ("Login Successfully"));
            }
            else{
                JOptionPane.showMessageDialog(null, ("Login Failed! Please Try Again"));
                stdName.setText("");
                stdPass.setText("");
            }
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        
        StudentPage2 sPage2 = new StudentPage2();
        sPage2.show();

        dispose();
    }//GEN-LAST:event_stdloginActionPerformed

