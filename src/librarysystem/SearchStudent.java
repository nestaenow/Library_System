    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            ps = con.prepareStatement("select First_Name, Last_Name, Department, Matricule, Email, Phone_Number from students where Matricule = ?");
            String Matricule = matTab.getText();
            ps.setString(1, Matricule);
            ResultSet rsl = ps.executeQuery();
            if(rsl.next() == false)
            {
                JOptionPane.showMessageDialog(this, "Record not Found");
                fNameTb.setText("");
                lNameTb.setText("");
                deptTb.setText("");
                matTb.requestFocus();
                emailTb.setText("");
                telTb.setText("");
            }
            else{
                fNameTb.setText(rsl.getString("First_Name"));
                lNameTb.setText(rsl.getString("Last_Name"));
                deptTb.setText(rsl.getString("Department"));
                matTb.setText(rsl.getString("Matricule"));
                emailTb.setText(rsl.getString("Email"));
                telTb.setText(rsl.getString("Phone_Number"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(SearchISBN.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchBtnActionPerformed

